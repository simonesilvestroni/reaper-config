local dm = false
local function dMsg(str)
   if dm then reaper.ShowConsoleMsg(tostring(str) .. "\n") end
end


local function parseName()

    -- Cheers @cfillion
    local name = ({reaper.get_action_context()})[2]:match("([^/\\_]+).lua$")

    local toolbar, button = name:match("Press (.+) button (%d+)")
    dMsg("parsed name, got:\n" .. tostring(toolbar) .. ", " .. tostring(button))
    return toolbar, button

end


local function loadINI(name)

    dMsg("loadINI...")
    if not name then return end

    local file, err = io.open( reaper.GetResourcePath() .. "/" .. name .. ".ini", "r")
    if not file then
        reaper.MB("Error opening " .. name .. ".ini\n\n" .. tostring(err), "Whoops!", 0)
        return
    end

    local str = file:read("*all")
    file:close()

    dMsg("loadINI returning: " .. str and str:len())
    return str

end



local function searchINI(toolbar, button)

    dMsg("searchINI, looking for:\n" .. toolbar .. ", " .. button)
    local ini = loadINI("reaper-menu")
    if not ini then return end

    dMsg("checking lines")
    local in_toolbar
    for line in ini:gmatch("[^\n\r]+") do

        dMsg("\t" .. line)

        if line:sub(1,1) == "[" then
            in_toolbar = (line == "[" .. toolbar .. "]")
            dMsg("\tin_toolbar = " .. tostring(in_toolbar))
        elseif in_toolbar then
            local str = line:match("item_" .. button .. "=([%S]+)")
            if str then
                dMsg("\tFound a match for toolbar: " .. toolbar .. ", item " .. button .. ":")
                dMsg("\t" .. line)
                return str
            end
        end

    end

end


-- Make sure the action exists, convert from a named command, etc
local function validateActionID(action)

    if tonumber(action) then return action end
    return reaper.NamedCommandLookup(action)

end


local function findActionID(toolbar, button)

    dMsg("findActionID...")

    local action = searchINI(toolbar, button)
    if not action then return end

    dMsg("findActionID got action: " .. tostring(action))

    return validateActionID(action)

end



local function Main()

    local toolbar, button = parseName()
    if not toolbar or not button then return end

    local action = findActionID(toolbar, button)
    if not action then 
        dMsg("no valid action found")
        return 
    end

    dMsg("Running action: " .. action)
    reaper.Main_OnCommand(action, 0)

end

Main()